package com.tutorialspoint;

public class Student {
	private int age;
	private String name;
	private int id;
	
	//methods
	public void setAge(Integer age){
		this.age = age;
	}
	
	public Integer getAge(){
		return age;
	}
	
	public void setName(String name){
		this.name = name;
	}
	
	public String getName(){
		return name;
	}
	
	public void setId(Integer Id){
		this.id = Id;
	}
	
	public Integer getId(){
		return id;	
	}

}
